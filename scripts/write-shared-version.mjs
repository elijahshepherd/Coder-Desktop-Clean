import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const packageJsonPath = resolve("package.json");
const versionFilePath = resolve("src", "shared", "version.ts");
const rootVersionPath = resolve("VERSION");
const packageJson = JSON.parse(await readFile(packageJsonPath, "utf8"));
const version = String(packageJson.version ?? "").trim();

if (!/^\d+\.\d+\.\d+(?:[-+][0-9A-Za-z.-]+)?$/.test(version)) {
  throw new Error(`package.json has an invalid version: ${version || "(empty)"}`);
}

await writeFile(
  versionFilePath,
  `// Generated by scripts/write-shared-version.mjs. Do not edit by hand.\nexport const appVersion = ${JSON.stringify(version)};\n`,
  "utf8"
);

await writeFile(rootVersionPath, `${version}\n`, "utf8");
